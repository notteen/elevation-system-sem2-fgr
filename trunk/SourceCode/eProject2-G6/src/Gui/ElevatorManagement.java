    private void tbElevatorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbElevatorMouseClicked
        if (evt.getClickCount() == 2) {
            JTable target = (JTable) evt.getSource();
            int row = target.getSelectedRow();
            Elevator e = elevatorModel.getElevator(row);
            ElevatorViewEdit eve = new ElevatorViewEdit(this, true, e, 1, false);
            eve.setVisible(true);
        } else {
            JTable target = (JTable) evt.getSource();
            int row = target.getSelectedRow();
            Elevator e = elevatorModel.getElevator(row);
            viewImage(e.getImage());
        }
    }//GEN-LAST:event_tbElevatorMouseClicked

