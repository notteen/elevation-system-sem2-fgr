    public void initimage() {
        ImageIcon icon = new javax.swing.ImageIcon(getClass().getResource("/Images/no-available-image.png"));
        Image img = icon.getImage();
        float width = icon.getIconWidth();
        int height = icon.getIconHeight();
        int h = height;
        int w = (int) width;
        if (width > 135) {
            h = (int) (height / (width / 135));
            w = 135;
        }
        Image newimg = img.getScaledInstance(w, h, java.awt.Image.SCALE_SMOOTH);
        Icon newIcon = new ImageIcon(newimg);
        lbimage.setIcon(newIcon);
    }

    public void viewImage(String image) {
        File fdir = new File(System.getProperty("java.class.path"));
        File dir = fdir.getAbsoluteFile().getParentFile();
        String path = dir.toString();
        ImageIcon icon = new javax.swing.ImageIcon(path + "/Images/" + image);
        Image img = icon.getImage();
        float width = icon.getIconWidth();
        int height = icon.getIconHeight();
        int h = height;
        int w = (int) width;
        if (width > 135) {
            h = (int) (height / (width / 135));
            w = 135;
        }
        Image newimg = img.getScaledInstance(w, h, java.awt.Image.SCALE_SMOOTH);
        Icon newIcon = new ImageIcon(newimg);
        lbimage.setIcon(newIcon);
    }

